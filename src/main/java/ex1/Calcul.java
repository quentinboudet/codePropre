package ex1;

/**
 * Classe pour faire des opértions mathématique
 * @author ETY1
 *
 */
public class Calcul {

	/**
	 * @param a	un nombre
	 * @param b un autre nombre
	 * @return int
	 */
	public int add(int a, int b) {
		return a + b;
	}

	/**
	 * @param a un nombre
	 * @param b un autre nombre
	 * @return int
	 */
	public int subtract(int a, int b) {
		return a - b;
	}
}
